package main;

/**
 * Class for X Y coordinates on the map
 */

public class Coordinate {

    /**
     * Instance variables for coordinates X and Y
     */
    private int x, y;

    /**
     * Getter for coordinate X
     *
     * @return value of X
     */
    public int getX() {
        return x;
    }

    /**
     * Setter for coordinate X
     *
     * @param x set value of X
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * Getter for coordinate Y
     *
     * @return value of Y
     */
    public int getY() {
        return y;
    }

    /**
     * Setter for coordinate Y
     *
     * @param y set value of Y
     */
    public void setY(int y) {
        this.y = y;
    }
}
